package com.example.demo;

public class LocationResponse {
	
	public int loc_count;
	public int loc_response;
	public int loc_id;
	
	public int getLoc_count() {
		return loc_count;
	}
	public void setLoc_count(int loc_count) {
		this.loc_count = loc_count;
	}
	public int getLoc_response() {
		return loc_response;
	}
	public void setLoc_response(int loc_response) {
		this.loc_response = loc_response;
	}
	public int getLoc_id() {
		return loc_id;
	}
	public void setLoc_id(int loc_id) {
		this.loc_id = loc_id;
	}


}
